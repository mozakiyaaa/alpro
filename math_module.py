import math

akar = math.sqrt(25)
pangkat = math.pow(4,3)
bulat_bawah = math.floor(1.6)
bulat_atas = math.ceil(1.6)
print()
print(f"akar 25 = {akar}")
print(f"4 pangkat 3 = {pangkat}")
print(f"Pembulatan ke bawah dari 1.6 = {bulat_bawah}")
print(f"Pembulatan ke atas dari 1.6 = {bulat_atas}")
print()